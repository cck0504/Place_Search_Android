package com.example.chillysoup.searchplace;

public class YelpData {
    private String name_yelp;
    private String lat_yelp;
    private String lng_yelp;
    private String city_yelp;
    private String state_yelp;
    private String country_yelp;
    private String postal_code_yelp;
    private String address1_yelp;
    private String phone_yelp;

    public YelpData(String name_yelp, String lat_yelp, String lng_yelp, String city_yelp, String state_yelp, String country_yelp, String postal_code_yelp, String address1_yelp, String phone_yelp) {
        this.name_yelp = name_yelp;
        this.lat_yelp = lat_yelp;
        this.lng_yelp = lng_yelp;
        this.city_yelp = city_yelp;
        this.state_yelp = state_yelp;
        this.country_yelp = country_yelp;
        this.postal_code_yelp = postal_code_yelp;
        this.address1_yelp = address1_yelp;
        this.phone_yelp = phone_yelp;
    }

    public String getName_yelp() {
        return name_yelp;
    }

    public void setName_yelp(String name_yelp) {
        this.name_yelp = name_yelp;
    }

    public String getLat_yelp() {
        return lat_yelp;
    }

    public void setLat_yelp(String lat_yelp) {
        this.lat_yelp = lat_yelp;
    }

    public String getLng_yelp() {
        return lng_yelp;
    }

    public void setLng_yelp(String lng_yelp) {
        this.lng_yelp = lng_yelp;
    }

    public String getCity_yelp() {
        return city_yelp;
    }

    public void setCity_yelp(String city_yelp) {
        this.city_yelp = city_yelp;
    }

    public String getState_yelp() {
        return state_yelp;
    }

    public void setState_yelp(String state_yelp) {
        this.state_yelp = state_yelp;
    }

    public String getCountry_yelp() {
        return country_yelp;
    }

    public void setCountry_yelp(String country_yelp) {
        this.country_yelp = country_yelp;
    }

    public String getPostal_code_yelp() {
        return postal_code_yelp;
    }

    public void setPostal_code_yelp(String postal_code_yelp) {
        this.postal_code_yelp = postal_code_yelp;
    }

    public String getAddress1_yelp() {
        return address1_yelp;
    }

    public void setAddress1_yelp(String address1_yelp) {
        this.address1_yelp = address1_yelp;
    }

    public String getPhone_yelp() {
        return phone_yelp;
    }

    public void setPhone_yelp(String phone_yelp) {
        this.phone_yelp = phone_yelp;
    }
}
